/*
Ejemplos de 
tipos de datos
en JavaScript
*/
//Tipo de dato string
var nombre = "Mauro";
console.log(typeof nombre);

//tipo de dato numerico
var numero = 8;
console.log(typeof numero);

//tipo de dato boolean
var bool = true;
console.log(typeof bool);

//tipo de dato object
var object = {
    nombre:"Mauro",
    apellido:"Ciro",
    tel: 3298475664
};
console.log(typeof object);